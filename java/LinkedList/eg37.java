import java.util.LinkedList;

//37. Write a Java program that swaps two elements in a linked list.
public class eg37 {
    public static void main(String[] args) {
        LinkedList<Integer> linkedList = new LinkedList<>();
        linkedList.add(2);
        linkedList.add(3);
        linkedList.add(4);
        linkedList.add(5);
        linkedList.addFirst(1);

        System.out.println();
    }
}
